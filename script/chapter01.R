# 1.6.3 関数を作ってみよう ---------------------------------------------------------

varp <- function(x) {
  標本分散 <- var(x) * (length(x) -1)/length(x)
  return(標本分散)
}

x <- c(10, 13, 8, 15, 8)

print(var(x))
print(varp(x))


